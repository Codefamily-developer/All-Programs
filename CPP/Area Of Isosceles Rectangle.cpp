/*Area Of Isosceles Rectangle*/

#include <iostream>

using namespace std;

int main()
{   
    int b,h,a;
    cout<<"Enter Base of Isosceles Triangle :";
    cin>>b;
    cout<<"Enter Height of Isosceles Triangle :";
    cin>>h;
    a=0.5*b*h;
    cout<<endl;
    cout<<"Area of Isosceles triangle is :"<<a;
    return 0;
}

/*OUTPUT

Enter Base of Isosceles Triangle :3
Enter Height of Isosceles Triangle :2

Area of Isosceles triangle is :3

*/