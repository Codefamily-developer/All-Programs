/*Area Of Rectangle*/

#include <iostream>

using namespace std;

int main()
{   
    int l,w,a;
    cout<<"Enter Length of Rectangle :";
    cin>>l;
    cout<<"Enter Width of Rectangle :";
    cin>>w;
    a=l*w;
    cout<<endl;
    cout<<"Area of Rectangle is :"<<a;
    return 0;
}

